# Arithmetic operators
print(2 + 4)
print(4 - 2)
print(4 * 3)
print(12 / 4)
print(24 // 5)    # Round
print(10 % 3)     # Modulo
print(2 ** 3)

# String Concatenation
print("Roqeeb " + "Oduja")

# Assignment Operator
meaning = 42
meaning += 1
print(meaning)

meaning -= 1
print(meaning)

meaning *= 10
print(meaning)

meaning /= 10
print(meaning)

# Comparison Operator
print(10 > 5)
print(4 < 3)
print(10 == 15)
print(42 != 4)
print(4 >= 10)
print(10 <= 5)

# Logical/boolean Operator
# and - or - not
print((5 > 3) and (2 < 4))
print((5 > 3) or (2 < 4))
print(not(5 > 3))
